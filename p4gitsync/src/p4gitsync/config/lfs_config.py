from dataclasses import dataclass, field


@dataclass
class LfsConfig:
    enabled: bool = False
    extensions: list[str] = field(default_factory=lambda: [
        ".uasset", ".umap", ".fbx", ".png", ".jpg", ".jpeg",
        ".tga", ".psd", ".wav", ".mp3", ".ogg", ".mp4",
        ".bin", ".dll", ".so", ".exe",
    ])
    size_threshold_bytes: int = 100 * 1024  # 100KB

    def generate_gitattributes(self) -> str:
        """LFS 대상 확장자에 대한 .gitattributes 내용 생성."""
        lines = ["# Git LFS tracking rules (auto-generated by p4gitsync)"]
        for ext in sorted(self.extensions):
            lines.append(f"*{ext} filter=lfs diff=lfs merge=lfs -text")
        return "\n".join(lines) + "\n"
